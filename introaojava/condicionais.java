package introaojava;

public class condicionais {

    public static void main(String[] args) {

        int nota = 70;

        // nota maior ou igual 70, então aprovado
        // nota menor que 70, então reprovado

        if (nota >= 70) {
            System.out.println("Aprovado");
        } else {
            System.out.println("Reprovado");
        }
    }
}
